def reconstruct_key(table_a, table_b, table_c, table_d):
    """
    Reconstruct the AES-128 key from the scattered lookup tables.
    This mimics the logic in the binary's reconstruct_key function.
    """
    tables = [table_a, table_b, table_c, table_d]
    
    # Key reconstruction indices
    key_indices = [
        (0, 4),   (1, 17),  (2, 10),  (3, 20),
        (0, 12),  (1, 25),  (2, 9),   (3, 22),
        (0, 30),  (1, 3),   (2, 21),  (3, 6),
        (0, 24),  (1, 19),  (2, 27),  (3, 31)
    ]
    
    key = bytearray(16)
    
    for i in range(16):
        table_idx1 = key_indices[i][0]
        table_idx2 = key_indices[i][1]
        
        # XOR values from two different tables
        val1 = tables[table_idx1][table_idx2]
        val2 = tables[(table_idx1 + 2) % 4][key_indices[i][0] * 8 + (i % 8)]
        
        key[i] = val1 ^ val2
    
    return bytes(key)


def decrypt_data(encrypted_data, key):
    """
    Decrypt the data using the reconstructed key.
    This mimics the binary's decrypt_data function.
    """
    decrypted = bytearray(len(encrypted_data))
    
    for i in range(len(encrypted_data)):
        decrypted[i] = encrypted_data[i] ^ key[i % 16] ^ SBOX[(i * 7) % 256]
    
    return bytes(decrypted)


