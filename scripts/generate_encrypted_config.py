    # Encrypt the configuration
    encrypted = encrypt_data(config_json, key)
    print(f"\nEncrypted data ({len(encrypted)} bytes):")
    
    # Print as C array
    print("\nstatic const uint8_t encrypted_data[{}] = {{".format(len(encrypted)))
