// Reconstruct the AES key from scattered lookup tables
void reconstruct_key(uint8_t *key) {
    const uint8_t* tables[4] = {table_a, table_b, table_c, table_d};
    
    for (int i = 0; i < 16; i++) {
        uint8_t table_idx1 = key_indices[i][0];
        uint8_t table_idx2 = key_indices[i][1];
        
        // XOR values from two different tables
        uint8_t val1 = tables[table_idx1][table_idx2];
        uint8_t val2 = tables[(table_idx1 + 2) % 4][key_indices[i][0] * 8 + (i % 8)];
        
        key[i] = val1 ^ val2;
    }
}

// Simple XOR-based decryption (simplified for demonstration)
void decrypt_data(const uint8_t *encrypted, uint8_t *decrypted, size_t len, const uint8_t *key) {
    for (size_t i = 0; i < len; i++) {
        decrypted[i] = encrypted[i] ^ key[i % 16] ^ sbox[(i * 7) % 256];
    }
}

